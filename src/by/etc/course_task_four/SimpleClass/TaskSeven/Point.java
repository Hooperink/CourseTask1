package by.etc.course_task_four.SimpleClass.TaskSeven;

public class Point {
    private double x;
    private double y;
    Point(){
        x = 0;
        y = 0;
    }
    Point(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
